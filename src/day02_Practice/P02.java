package day02_Practice;

public class P02 {

    public static void main(String[] args) {

        // Konsola Hello World ve "Hello World" yazdırınız.

        System.out.println("Hello World ve `\"Hello World\" ");

        /*
        Konsola tek satırda kod yazarak

                Hello World
                "Hello World"
         */

        System.out.println("Hello World\n\" Hello World\"");

        /*
        Konsola Hello world  "Hello World" yazdırın  (arada 2 tab tuşu mesafe olacak)

         */

        System.out.println("Hello World\t\t\"HelloWorld\"");

        /*
        /*
        Konsola   Hello World \Hello World\ yazdıralım
         */


        System.out.println("Hello World\t\t\\Hello World\\");
        System.out.println("\"");






        /*

        \" : " yazdırır
        \' :' yazdırır
        \n : alt satırdan yazdırmaya devam eder
        \t  : bir tab tuşu kadar boşluk bırakır
        \\ :\ yazar

         */
    }
}
