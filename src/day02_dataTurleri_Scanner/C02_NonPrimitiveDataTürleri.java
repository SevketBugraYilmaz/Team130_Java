package day02_dataTurleri_Scanner;

public class C02_NonPrimitiveDataTürleri {
    public static void main(String[] args) {

        // primitive data türleri sadece deger alirlar
        // non-primitive data türleri ise degerler ile birlikte method'lar da barındırırlar

        String str ="Java Candir";

        int sayı = 20;

        System.out.println(sayı); // 20

        System.out.println(str.toUpperCase()); // JAVA CANDİR

        System.out.println(str); //

        System.out.println(str.toLowerCase()); // java candir


    }
}
